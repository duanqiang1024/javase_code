package com.itheima_01;

public class MethodDemo {
    public static void main(String[] args) {
        isEvenNumver();
    }

    public static void isEvenNumver() {
        int num = 10;
        if (num % 2 == 0) {
            System.out.println(true);
        } else {
            System.out.println(false);
        }

    }
}
